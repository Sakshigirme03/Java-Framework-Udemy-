

public class isEven {

    public static void isEvenNo(int n){
        if(n%2==0){
            System.out.println("Is Even");
        }
        else {
            System.out.println("Not Even number");
        }
    }
    public static void main(String[]args){
        isEvenNo(40);
        isEvenNo(33);

    }
}
